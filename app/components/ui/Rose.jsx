function draw() {
	var globalTime = millis()*0.001;
	pg.background(0, 1);
	pg.strokeWeight(6);
	pg.stroke(255);
	let factor = (globalTime % duration) / duration;
	if (!lastPos) {
		lastPos = iDFT(factor);
		return;
	}
	let pos = iDFT(factor);
	pg.line(lastPos.x+center.x, lastPos.y+center.y, pos.x+center.x, pos.y+center.y);
	lastPos = pos;
	image(pg, 0, 0);
	drawCycles(globalTime);
}

function drawCycles(t) {
	fill(0);
	text(svgPoints.length, 20, 20);
  let last = createVector(width*0.5, height*0.5);
  noFill();
  stroke(0, 255, 255, 200);
  for(let i = 0 ; i < min(/*cycleCount*/ 80, cs.length) ; i++) {
    strokeWeight(lerp(1, 3, float(i)/cs.length));
    let c = cs[i];
    let offset = createVector(cos(t*c.freq+c.phase), sin(t*c.freq+c.phase)).mult(c.amp);
    ellipse(last.x, last.y, c.amp*2, c.amp*2);
    line(last.x, last.y, last.x + offset.x, last.y + offset.y);
    last.add(offset);
  }
}

function processData() {
  datas = [];
  for(let i = 0 , N = svgPoints.length ; i < N ; i+=2) {
    datas.push(createVector(svgPoints[i]*base, -1*svgPoints[i+1]*base));
  }
  cs = DFT(datas);
  cs.sort(function(a, b) {
		return b.amp - a.amp;
	}); 
}

